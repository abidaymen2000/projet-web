        <div class="container">
            <h3>Les méthodes les plus populaires</h3>
            <p style="color:rgb(6, 139, 46);text-align:center;font-size:150%;">
                Parmi les <strong>différents types de musculation</strong>, certaines méthodes ont fait leurs preuves et sont très répandues dans l’univers de la salle. Zoom sur ces entraînements réputés !
            </p>
            <h4>Full body</h4>
            <p>
                Le Full Body implique de <strong>travailler la totalité du corps pendant une séance.</strong> Tous les muscles sont sollicités et on retrouve lors d’un entraînement la plupart des mouvements de base. Ce <strong>type de travail</strong> peut être réalisé sur deux ou trois séances par semaine : comme tous les groupes musculaires sont mis à rude épreuve, il est important d’observer des jours de repos.
            </p>
            <h4>Split body</h4>
            <p>
                Contrairement au Full body, <strong>ce type d’entraînement se focalise sur quelques groupes musculaires lors d’une séance.</strong> Par exemple, vous allez travailler le lundi les abdominaux, les cuisses et les fessiers le lendemain et les épaules et les triceps le jour suivant. En travaillant ainsi, vos muscles auront le temps de récupérer et vous pourrez réaliser des entraînements pratiquement tous les jours (conservez tout de même un jour de repos total par semaine).
            </p>
            <h4>Cross Fit</h4>
            <p>
                Le Cross Fit fait de nombreux nouveaux adeptes chaque jour ! <strong>Cette méthode d’entraînement américaine</strong> combine <strong>des exercices de cardio à des séries de musculation.</strong> Une séance dure généralement entre 30 et 45 minutes et se révèle être très intense.
            </p>
            <h4>Le Hiit</h4>
            <p>
                « L’entraînement par intervalles à haute intensité » est un <strong>type de musculation</strong> qui propose <strong>des phases d’exercices à haute intensité, combinées à des étapes de récupération active.</strong> Très plébiscitée par les personnes qui manquent de temps et qui souhaitent des résultats rapides, cette méthode augmente vos performances grâce à des exercices très courts mais très intenses. Elle regroupe :
            </p>
            <h5>La méthode Tabata</h5>
            <p>
                C’est un travail <strong>cardio très intense et très court</strong> qui s’effectue sur des séries de 20 secondes, suivies d’une pause de 10 secondes. Le circuit doit être réalisé 8 fois.
            </p>
            <h5>La méthode Little</h5>
            <p>
                Sur le même principe, <strong>ce type d’entraînement</strong> consiste à travailler pendant 60 secondes puis à observer une phase de récupération de 75 secondes.
            </p>
            <h4>Méthode Lafay</h4>
            <p>
                Cette <strong>méthode</strong> est <strong>très répandue et appréciée</strong> car elle permet de réaliser des exercices sans aucun matériel, uniquement au poids de corps. Très pratique, elle inclue des mouvements basiques et simples à mémoriser, à effectuer n’importe où.
            </p>
            <h4>Le TRX</h4>
            <p>
                Le « Total Résistance Exercice » s’effectue au poids de corps, grâce à des sangles ajustables. De plus en plus présent dans les salles de sport, le TRX est un moyen efficace de s’entraîner sans charge extérieure et de varier les exercices, pour casser la routine.
            </p>
