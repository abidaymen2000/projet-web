    <section id="type" class="musculation">
        <div class="container">
            <h3>Musculation au poids de corps et musculation avec charges</h3>
            <p>
                Nous verrons plus en détail <strong>les exercices à pratiquer en fonction de votre objectif</strong> et <stong>les méthodes de musculation les plus réputées</stong>, dans les paragraphes suivants. Il est cependant important de faire la différence entre deux types d’entraînement de musculation :
            </p>
            <p>
                <strong>La musculation au poids de corps</strong> : Cette <strong>méthode de travail</strong> ne nécessite aucun accessoire, vous n’aurez besoin que de votre corps pour la pratiquer. Elle est essentielle lorsque l’on débute : il est évidemment important de d’abord maîtriser les mouvements avant d’y ajouter les charges. Mais elle peut aussi convenir à des pratiquants de musculation d’un niveau plus avancé, car différentes astuces permettent d’ajouter de la difficulté, sans rajouter de charges externes.
            </p>
            <p>
                De nombreuses idées reçues laissent à penser que la musculation au poids de corps n’est plus efficace à force de s’entraîner : c’est tout à fait faux. <strong>Ce type de musculation</strong> peut être <strong>beaucoup plus intense qu’avec des charges</strong>, selon le positionnement de votre corps ou selon les appuis au sol.
            </p>
            <p>
                <strong>La musculation avec poids libres</strong> : Ce <strong>type de travail</strong> implique de réaliser les mouvements <strong>en ajoutant une charge grâce à des haltères, des disques, des kettlebells.</strong> En ajoutant du poids, vous augmentez l’intensité de l’entraînement et vous invitez votre muscle à travailler davantage. Les poids libres vous donnent l’avantage de pouvoir ajuster la charge en fonction de votre niveau et de suivre plus facilement votre évolution.
            </p>
            <img src="../assetes/img/body3.jpg" alt="body3" style="width:336px;height:450px;">
            <br />
            <p>
                Il faut savoir que <strong>ces deux types d’entraînement</strong> peuvent tout à fait être complémentaires et qu’il n’existe pas une méthode plus efficace que l’autre.Vous pouvez tout à fait <strong>inclure des exercices au poids de corps et des séries avec des charges dans votre programme de musculation.</strong>
            </p>
            <p style="color:rgb(6, 139, 46);text-align:center;font-size:150%;"> 
                Justement, casser la routine est un excellent moyen de rebooster votre motivation et d’obtenir des résultats probants.
            </p>
        
            <h3>Le type de musculation selon votre objectif</h3>
           <p style="color:rgb(6, 139, 46);text-align:center;font-size:150%;">
            Si différentes méthodes d’entraînement existent, il est primordial de <strong>définir vos objectifs</strong> avant de vous orienter vers un type de musculation.
           </p>
           <h4 style="font-family:Optima;">Sèche et perte de gras</h4>
           <p>
            Si vous débutez la musculation avec pour objectif de perdre votre gras et de sécher, vous devrez adapter vos séances. Pour perdre du poids rapidement, il est important d’inclure autant de cardio dans votre entraînement que de musculation. L’équilibre entre les deux est primordial car c’est ce qui vous permettra de ne pas perdre de masse musculaire.
           </p>
           <p>
            Un programme mixte est une alternative idéale pour avoir un impact considérable sur la perte de gras : il sera impossible d’obtenir les mêmes résultats en effectuant uniquement du cardio ou de la musculation. Dans votre programme de sèche, il faudra recourir à des charges légères à moyennes : votre régime alimentaire vous imposera des restrictions caloriques, vos capacités seront donc réduites.
           </p>
           <h4>Prise de masse</h4>
           <p>
            La prise de masse est une phase d’entraînement intense et spécifique pour prendre de la masse musculaire rapidement. Cette étape doit intervenir après une sèche car elle n’est pas pertinente si votre taux de gras est trop important. La prise de masse doit s’envisager uniquement si votre physique est adapté.
           </p>
           <p>
            Ce type de musculation va impliquer d’effectuer des exercices de base avec des charges lourdes. Pour ne pas stagner, vous devrez être réactif et augmenter régulièrement vos charges. Votre muscle s’habituera rapidement à l’effort et ne progressera plus si vous ne variez pas les poids. Vous devez atteindre une intensité maximale en entraînement.
           </p>
        
            <h3 style=>Le type de musculation selon votre morphologie</h3>
            <p style="color:rgb(6, 139, 46);text-align:center;font-size:150%;">
                Le type de musculation pourra se définir également <strong>en fonction de votre morphologie.</strong> Même si vous rêvez d’avoir des muscles de bodybuilder, il faut savoir que certains corps ne s’y prêtent pas et que votre morphologie de base sera déterminante dans votre programme d’entraînement.
            </p>
            <h4>Vous êtes ectomorphe</h4>
            <p>
                <strong>Ce type de morphologie</strong> correspond aux personnes minces. Ce sont généralement celles qui peuvent manger de tout et qui prendront rarement du poids. Dans ce cas de figure, cela signifie aussi <strong>qu’il sera plus difficile de prendre de la masse musculaire.</strong> L’ectomorphe devra privilégier un entraînement court avec des exercices de base. Pour prendre du muscle, il devra prendre du poids et donc multiplier les repas.
            </p>
            <h4>Vous êtes endomorphe</h4>
            <p>
                Les endomorphes ont des os plus lourds ainsi qu’une taille et des hanches plus larges. Ils ne rencontrent aucun problème pour prendre du poids mais éprouvent plus de difficultés pour s’en débarrasser. L’endomorphe ne devra pas seulement s’attarder sur la musculation mais aussi <strong>réaliser des entraînements de cardio réguliers.</strong> Concernant le <strong>type de musculation</strong>, il devra s’entraîner avec des charges modérées.
            </p>
            <h4>Vous êtes mésomorphe</h4>
            <p>
                Cette morphologie est idéale pour être bodybuilder. Caractérisée par un physique taillé « en V », elle permet de <strong>supporter une grande masse musculaire</strong> tout en conservant une belle harmonie. Les mésomorphes pourront miser sur un <strong>entraînement long et intense</strong> et pratiquer le cardio de manière modérée.
            </p>
